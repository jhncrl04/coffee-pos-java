    private void saveProfileChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveProfileChangeActionPerformed
        // TODO add your handling code here:
        
        String newFName = newFNameField.getText();
        String newLName = newLNameField.getText();
        String newEmail = newEmailField.getText();
        String newContact = newContactField.getText();
        
        if (newFNameField.getText().isEmpty() || newEmailField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Don't leave your first name and email empty");
        }
        else if(!(newFName.equals(CoffeeSystem.userFName) && newLName.equals(CoffeeSystem.userLName) && newEmail.equals(CoffeeSystem.userEmail))){
            DBQuery.updateUserInfo();
        }
        else if(newContactField.getText().isEmpty() && CoffeeSystem.userContact != null){
            DBQuery.updateUserInfo();
        }
        else if(!newContactField.getText().isEmpty() && !(newContact.equals(CoffeeSystem.userContact))){
            DBQuery.updateUserInfo();
        }
        else if(!(methods.getSelectedButton(genderBtnGrp) == null) && CoffeeSystem.userGender == null){
            DBQuery.updateUserInfo();
        }
        else if(!(methods.getSelectedButton(genderBtnGrp) == null) && !methods.getSelectedButton(genderBtnGrp).equals(CoffeeSystem.userGender)){
            DBQuery.updateUserInfo();
        }
        else{
            JOptionPane.showMessageDialog(null, "Can't save\nNothing is changed");
        }
        
        if(CoffeeSystem.userGender == null || CoffeeSystem.userGender.equals("Male")){
            ImageIcon image = new ImageIcon(getClass().getResource("/images/male-icon.png"));

            jLabel1.setIcon(image);
            jLabel1.setText(" My Profile");
        }
        else{
            ImageIcon image = new ImageIcon(getClass().getResource("/images/female-icon.png"));

            jLabel1.setIcon(image);
            jLabel1.setText(" My Profile");
        }
    }//GEN-LAST:event_saveProfileChangeActionPerformed

