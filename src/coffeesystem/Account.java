    private void changePassBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePassBtnActionPerformed
        // TODO add your handling code here:
        
        if(oldPassField.getText().isEmpty() || newPassField.getText().isEmpty() || confirmPassField.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please fill all the fields to continue");
            
            oldPassField.setText("placeholder");
            newPassField.setText("");
            confirmPassField.setText("");
        }
        else if(!CoffeeSystem.hashedPassword.equals(methods.passwordHashing(oldPassField.getText()))){
            JOptionPane.showMessageDialog(null, "Incorrect old password\nTry again");
            
            oldPassField.setText("placeholder");
            newPassField.setText("");
            confirmPassField.setText("");
        }
        else if(CoffeeSystem.hashedPassword.equals(methods.passwordHashing(newPassField.getText()))){
            JOptionPane.showMessageDialog(null, "New password must be different from your old password");
            
            oldPassField.setText("placeholder");
            newPassField.setText("");
            confirmPassField.setText("");
        }
        else{
            if( newPassField.getText().equals(confirmPassField.getText())){
                String newHashedPass = methods.passwordHashing(newPassField.getText());

                DBQuery.updatePassword(newHashedPass, CoffeeSystem.userEmail);
                
                oldPassField.setText("placeholder");
                newPassField.setText("");
                confirmPassField.setText("");
            }
            else{
                JOptionPane.showMessageDialog(null, "New password don't match\nTry again");
            }
        }
    }//GEN-LAST:event_changePassBtnActionPerformed

